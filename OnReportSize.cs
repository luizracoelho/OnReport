namespace OnReport
{
    /// <summary>
    /// Definitions of report size.
    /// </summary>
    public class OnReportSize
    {
        /// <summary>
        /// Page Width: Value + Scale.
        /// </summary>
        public string PageWidth { get; set; }

        /// <summary>
        /// Page Height: Value + Scale.
        /// </summary>
        public string PageHeight { get; set; }

        /// <summary>
        /// Margin Top: Value + Scale.
        /// </summary>
        public string MarginTop { get; set; }

        /// <summary>
        /// Margin Bottom: Value + Scale.
        /// </summary>
        public string MarginBottom { get; set; }

        /// <summary>
        /// Margin Left: Value + Scale.
        /// </summary>
        public string MarginLeft { get; set; }

        /// <summary>
        /// Margin Right: Value + Scale.
        /// </summary>
        public string MarginRight { get; set; }
    }
}
